// Node.js script to generate build-info.ts with current ISO timestamp
const fs = require('fs');
const path = require('path');

const filePath = path.join(__dirname, 'src', 'build-info.ts');
const timestamp = new Date().toISOString();
const content = `// This file is automatically generated at build time.\nexport const BUILD_TIMESTAMP = '${timestamp}';\n`;

fs.writeFileSync(filePath, content);
console.log(`Generated build-info.ts with timestamp: ${timestamp}`);
